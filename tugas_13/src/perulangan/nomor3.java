package perulangan;
public class nomor3 {
    public static void main(String[] args){
        //deklarasi variabel i dengan nilai 1
        int i = 1;
        //perulangan do while
        do{
            //mencetak output
            System.out.println("Belajar Perulangan Do While " + i);
            //increment i
            i++;
        //mengecek apakah i <= 10    
        }while(i <= 10);
    }
}
