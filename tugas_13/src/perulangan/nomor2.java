package perulangan;
public class nomor2 {
    public static void main(String[] args){
        //deklarasi variabel i dengan nilai 1
        int i = 1;
        //perulangan while ketika i kurang dari atau sama dengan 10
        while(i <= 10){
            //blok kode yang akan diulang ketika kondisi while bernilai True
            //mencetak output
            System.out.println("Belajar Perulangan While " + i);
            //increment i
            i++;
        }
    }
}
