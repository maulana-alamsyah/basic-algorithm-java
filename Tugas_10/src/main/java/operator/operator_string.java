package operator;
public class operator_string {
    public static void main(String[] args){
        //Deklarasi variabel namaAwal dan namaAkhir dengan tipe data string
        String namaAwal, namaAkhir;
        
        namaAwal = "Maulana "; //Memberi nilai Maulana pada variabel namaAwal
        namaAkhir = "Alamsyah"; //Memberi nilai Alamsyah pada variabel namaAkhir
        
        //menggabungkan dua variabel dan menampilkannya
        System.out.println(namaAwal + namaAkhir);
    }
}
